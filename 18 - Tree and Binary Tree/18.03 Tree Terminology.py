class Tree:
    '''
    Root: Top node without parent.

    Edge: A link between parent and child

    Leaf: A node which does not have children

    Sibling: Children with same parent

    Ancestor: Parent, grandparent, and great-grandparent of a node

    Depth of a node: Length of the path from root node to the node

    Height of node: Length of the path from node to the deepest node

    Depth of tree: depth of root

    height of tree: height of root
    '''


